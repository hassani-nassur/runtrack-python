def determineMetier(langage):
    if(langage == "javascript"):
        print(langage, " : Tu es un developpeur Web")
    elif(langage == "python"):
        print(langage, " : Tu es un developpeur IA")
    elif(langage == "java"):
        print(langage, " : Tu es developpeur logiciel")
    elif(langage == "reactjs"):
        print(langage, " : Tu es un developpeur mobile")
    else:
        print(langage, " : un jours, je serai un meilleur developpeur...")
        

determineMetier("java")
determineMetier("python")
determineMetier("reactjs")
determineMetier("javascript")
determineMetier("C#")
    